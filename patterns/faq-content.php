				<summary class="gvb-faq__question">Wie viel kosten unsere Trinkflaschen?</summary>
				<div class="gvb-faq__answer"><p>Die Preise hängen vom gewählten <a href="/unsere-flaschen/">Modell</a>, dem Zubehör, der Bestellmenge und einer möglichen <a href="/unsere-losungen/bedrucken/">Personalisierung</a> ab. Für alle Produkte bieten wir Preisstaffelungen an: Je höher die Bestellmenge, desto geringer der Stückpreis. Unsere aktuelle Preisliste findest du <a href="/download/">hier</a>.</p><p>Die Mindestbestellmenge liegt bei 50 Stück. Für ein individuelles Angebot schick uns einfach eine Anfrage über das Kontaktformular.</p></div>
			</details>

			<details class="gvb-faq__item">
				<summary class="gvb-faq__question">Wie robust und langlebig sind unsere Trinkflaschen?</summary>
				<div class="gvb-faq__answer"><p>Unsere Trinkflaschen bestehen aus <a href="/unsere-flaschen/edelstahl/">Edelstahl</a>, <a href="/unsere-flaschen/borosilikatglas/">Borosilikatglas</a> und <a href="/unsere-flaschen/tritan/">Tritan</a> und sind für den täglichen Einsatz gemacht. Sie sind robust, bruchsicher und deutlich widerstandsfähiger als viele herkömmliche Alternativen. Mit der richtigen Pflege bleiben sie über Jahre hinweg im Einsatz.</p></div>
			</details>

			<details class="gvb-faq__item">
				<summary class="gvb-faq__question">Sind unsere Trinkflaschen BPA-frei und schadstofffrei?</summary>
				<div class="gvb-faq__answer"><p>Ja, alle unsere <a href="/unsere-flaschen/">Trinkflaschen</a> sind BPA-frei sowie frei von BPS, Weichmachern und anderen Schadstoffen. Sie sind lebensmittelecht, entsprechen den geltenden Vorschriften und sind für den täglichen Gebrauch geeignet. Auf Wunsch stellen wir dir eine Konformitätserklärung zur Verfügung.</p></div>
			</details>

			<details class="gvb-faq__item">
				<summary class="gvb-faq__question">Wie robust sind unsere Glasflaschen und was macht ihr Design besonders?</summary>
				<div class="gvb-faq__answer"><p>Unsere <a href="/unsere-flaschen/borosilikatglas/">Glasflaschen</a> sind so aufgebaut, dass sie im Alltag stabil und gleichzeitig angenehm leicht sind. Der speziell konstruierte Flaschenboden reduziert Spannungen im Glas und sorgt so für zusätzliche Widerstandsfähigkeit. Das Ergebnis: eine langlebige Lösung für den täglichen Einsatz – kombiniert mit einer klaren, modernen und eleganten Optik.</p></div>
			</details>

			<details class="gvb-faq__item">
				<summary class="gvb-faq__question">Wie kann eine Trinkflasche aus Tritan umweltfreundlich sein?</summary>
				<div class="gvb-faq__answer"><p>Kunststoff ist nicht gleich Kunststoff. Bei unseren <a href="/unsere-flaschen/tritan/">Tritan</a> Trinkflaschen handelt es sich um robuste und langlebige Mehrwegflaschen, die viele Einwegflaschen ersetzen. Je länger du sie nutzt, desto nachhaltiger werden sie. Und auch bei der richtigen Entsorgung können Materialien wiederverwertet und in den Kreislauf zurückgeführt werden.</p></div>
			</details>

